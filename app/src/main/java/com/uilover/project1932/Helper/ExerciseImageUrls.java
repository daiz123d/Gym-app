    /**
     * Lấy URL ảnh dựa trên tên bài tập (fuzzy match với độ chính xác cao hơn)
     */
    public static String getImageUrlForExercise(String exerciseTitle) {
        if (exerciseTitle == null) return PEXELS_WORKOUT_1;
        
        String lowerTitle = exerciseTitle.toLowerCase();
        
        // ========== NGỰC ==========
        if (lowerTitle.contains("ngực") || lowerTitle.contains("chest")) {
            if (lowerTitle.contains("push") || lowerTitle.contains("hít đất") || lowerTitle.contains("chống đẩy")) {
                return CHEST_PUSH_UP;
            }
            if (lowerTitle.contains("fly") || lowerTitle.contains("mở") || lowerTitle.contains("dumbbell fly")) {
                return CHEST_FLYES;
            }
            if (lowerTitle.contains("incline") || lowerTitle.contains("dốc lên") || lowerTitle.contains("ngực trên")) {
                return CHEST_INCLINE;
            }
            if (lowerTitle.contains("decline") || lowerTitle.contains("dốc xuống") || lowerTitle.contains("ngực dưới")) {
                return CHEST_DECLINE;
            }
            if (lowerTitle.contains("cable") || lowerTitle.contains("cáp") || lowerTitle.contains("chéo")) {
                return CHEST_CABLE;
            }
            if (lowerTitle.contains("bench") || lowerTitle.contains("đẩy tạ") || lowerTitle.contains("press")) {
                return CHEST_BENCH_PRESS;
            }
            return CHEST_MAIN;
        }
        
        // ========== LƯNG ==========
        if (lowerTitle.contains("lưng") || lowerTitle.contains("back") || lowerTitle.contains("lat")) {
            if (lowerTitle.contains("pull") || lowerTitle.contains("kéo xà") || lowerTitle.contains("xà đơn")) {
                return BACK_PULL_UP;
            }
            if (lowerTitle.contains("deadlift") || lowerTitle.contains("kéo tạ chết")) {
                return BACK_DEADLIFT;
            }
            if (lowerTitle.contains("pulldown") || lowerTitle.contains("kéo cáp")) {
                return BACK_LAT_PULLDOWN;
            }
            if (lowerTitle.contains("one") || lowerTitle.contains("một tay") || lowerTitle.contains("single")) {
                return BACK_ONE_ARM_ROW;
            }
            if (lowerTitle.contains("row") || lowerTitle.contains("kéo tạ")) {
                return BACK_ROW;
            }
            return BACK_MAIN;
        }
        
        // ========== CHÂN ==========
        if (lowerTitle.contains("chân") || lowerTitle.contains("leg") || lowerTitle.contains("thigh")) {
            if (lowerTitle.contains("squat") || lowerTitle.contains("ngồi xổm")) {
                return LEGS_SQUAT;
            }
            if (lowerTitle.contains("lunge") || lowerTitle.contains("chùng") || lowerTitle.contains("bước")) {
                return LEGS_LUNGE;
            }
            if (lowerTitle.contains("curl") || lowerTitle.contains("gập")) {
                return LEGS_LEG_CURL;
            }
            if (lowerTitle.contains("extension") || lowerTitle.contains("duỗi")) {
                return LEGS_LEG_EXTENSION;
            }
            if (lowerTitle.contains("press") || lowerTitle.contains("đẩy chân")) {
                return LEGS_LEG_PRESS;
            }
            return LEGS_MAIN;
        }
        
        // ========== MÔNG ==========
        if (lowerTitle.contains("mông") || lowerTitle.contains("glute") || lowerTitle.contains("hip")) {
            if (lowerTitle.contains("thrust") || lowerTitle.contains("đẩy hông")) {
                return GLUTES_HIP_THRUST;
            }
            if (lowerTitle.contains("bridge") || lowerTitle.contains("cầu")) {
                return GLUTES_BRIDGE;
            }
            return GLUTES_MAIN;
        }
        
        // ========== VAI ==========
        if (lowerTitle.contains("vai") || lowerTitle.contains("shoulder") || lowerTitle.contains("delta")) {
            if (lowerTitle.contains("lateral") || lowerTitle.contains("ngang") || lowerTitle.contains("bên")) {
                return SHOULDER_LATERAL_RAISE;
            }
            if (lowerTitle.contains("front") || lowerTitle.contains("trước")) {
                return SHOULDER_FRONT_RAISE;
            }
            if (lowerTitle.contains("rear") || lowerTitle.contains("sau") || lowerTitle.contains("delt")) {
                return SHOULDER_REAR_DELT;
            }
            if (lowerTitle.contains("press") || lowerTitle.contains("đẩy")) {
                return SHOULDER_PRESS;
            }
            return SHOULDER_MAIN;
        }
        
        // ========== BẮP TAY TRƯỚC ==========
        if (lowerTitle.contains("bắp tay trước") || lowerTitle.contains("biceps")) {
            if (lowerTitle.contains("hammer") || lowerTitle.contains("búa")) {
                return BICEPS_HAMMER_CURL;
            }
            if (lowerTitle.contains("preacher") || lowerTitle.contains("ghế")) {
                return BICEPS_PREACHER_CURL;
            }
            if (lowerTitle.contains("cable") || lowerTitle.contains("cáp")) {
                return BICEPS_CABLE_CURL;
            }
            if (lowerTitle.contains("curl") || lowerTitle.contains("cuốn")) {
                return BICEPS_CURL;
            }
            return BICEPS_MAIN;
        }
        
        // ========== BẮP TAY SAU ==========
        if (lowerTitle.contains("bắp tay sau") || lowerTitle.contains("triceps")) {
            if (lowerTitle.contains("dips") || lowerTitle.contains("hít xà") || lowerTitle.contains("khuỷu")) {
                return TRICEPS_DIPS;
            }
            if (lowerTitle.contains("overhead") || lowerTitle.contains("trên đầu") || lowerTitle.contains("extension")) {
                return TRICEPS_OVERHEAD;
            }
            if (lowerTitle.contains("close") || lowerTitle.contains("hẹp")) {
                return TRICEPS_CLOSE_GRIP;
            }
            if (lowerTitle.contains("pushdown") || lowerTitle.contains("đẩy xuống") || lowerTitle.contains("cáp")) {
                return TRICEPS_PUSHDOWN;
            }
            return TRICEPS_MAIN;
        }
        
        // ========== BỤNG ==========
        if (lowerTitle.contains("bụng") || lowerTitle.contains("abs") || lowerTitle.contains("abdominal")) {
            if (lowerTitle.contains("plank")) {
                return ABS_PLANK;
            }
            if (lowerTitle.contains("sit") || lowerTitle.contains("gập bụng")) {
                return ABS_SIT_UP;
            }
            if (lowerTitle.contains("leg raise") || lowerTitle.contains("nâng chân")) {
                return ABS_LEG_RAISE;
            }
            if (lowerTitle.contains("crunch")) {
                return ABS_CRUNCH;
            }
            return ABS_MAIN;
        }
        
        // ========== CẲNG TAY ==========
        if (lowerTitle.contains("cẳng tay") || lowerTitle.contains("forearm")) {
            return FOREARM_CURL;
        }
        
        // ========== BẮP CHÂN ==========
        if (lowerTitle.contains("bắp chân") || lowerTitle.contains("calf") || lowerTitle.contains("calves")) {
            return CALVES_RAISE;
        }
        
        // Default
        return PEXELS_WORKOUT_1;
    }
